        <h2>Alimentos que cachorro não pode comer: veja os motivos</h2>
        <p class="intro">Embora nossos cães muitas vezes implorem por um pedacinho do que estamos comendo, é importante saber que nem todos os alimentos são seguros para eles. Alguns alimentos comuns em nossa dieta podem ser tóxicos ou causar problemas graves de saúde para os cães. Descubra quais alimentos evitar e os motivos para protegê-los!</p>
        <img src="/cepet/usuario/img/animais/dog (4).jpg" alt="Alimentos que cachorro não pode comer">
        <h3>1. Chocolate</h3>
        <p>O chocolate contém teobromina, uma substância que é tóxica para os cães. Ela pode causar vômitos, diarreia, tremores, convulsões e até ser fatal em casos graves.</p>

        <h3>2. Cebola e alho</h3>
        <p>Esses alimentos contêm compostos que podem destruir os glóbulos vermelhos dos cães, levando à anemia. Mesmo em pequenas quantidades, podem ser perigosos.</p>

        <h3>3. Uvas e passas</h3>
        <p>Uvas e passas podem causar insuficiência renal aguda em cães. Mesmo uma pequena quantidade pode ser tóxica, então é melhor evitar totalmente.</p>

        <h3>4. Abacate</h3>
        <p>O abacate contém persina, uma substância que pode causar vômitos e diarreia nos cães. Além disso, o caroço pode ser um risco de engasgamento.</p>

        <h3>5. Álcool</h3>
        <p>Bebidas alcoólicas e alimentos que contenham álcool são extremamente perigosos para os cães. Eles podem causar intoxicação, vômitos, dificuldades respiratórias, e em casos graves, até coma.</p>

        <h3>6. Ossos cozidos</h3>
        <p>Embora os cães possam adorar roer ossos, ossos cozidos podem se quebrar em lascas afiadas, causando lesões graves no trato gastrointestinal.</p>

        <h3>7. Cafeína</h3>
        <p>Assim como o chocolate, a cafeína é tóxica para os cães. Pode levar a agitação, respiração acelerada, tremores e até morte.</p>

        <h3>8. Doces e chicletes com xilitol</h3>
        <p>O xilitol, um adoçante artificial encontrado em muitos produtos, pode causar uma perigosa queda no nível de açúcar no sangue dos cães, levando a convulsões, insuficiência hepática e até morte.</p>

        <h3>9. Laticínios</h3>
        <p>Embora alguns cães possam tolerar laticínios, muitos são intolerantes à lactose, o que pode levar a problemas digestivos, como diarreia e vômitos.</p>

        <h3>10. Sal e alimentos salgados</h3>
        <p>Excesso de sal pode levar à intoxicação por sódio, causando sede excessiva, vômitos e, em casos graves, convulsões.</p>

        <h3>Cuidado com os alimentos para o bem-estar do seu cão</h3>
        <p>A saúde do seu cachorro depende de uma alimentação equilibrada e segura. Ofereça sempre rações de boa qualidade e, em caso de dúvidas, consulte um veterinário antes de dar qualquer alimento diferente para o seu pet.</p>
